/**
 * @param {number[]} digits
 * @return {number[]}
 * Input: digits = [9]
  Output: [1,0]
  Explanation: The array represents the integer 9.
  Incrementing by one gives 9 + 1 = 10.
  Thus, the result should be [1,0].
 */
var plusOne = function (digits) {
  let remem = 1;
  const len = digits.length;
  for (let i = len - 1; i >= 0; i--) {
    if (remem === 0) return digits;
    let digit = digits[i] + remem;
    remem = 0;
    if (digit > 9) {
      remem = 1;
      digit -= 10;
    }
    digits[i] = digit;
  }
  if (remem === 1) {
    digits.unshift(remem);
  }
  return digits;
};

